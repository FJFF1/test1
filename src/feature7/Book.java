package feature7;

public class Book {
    private String title;
    private String author;

    public Book (String title, String author) {
       this.title=title;
       this.author=author;
    }


    public String getTitle(){
        return title;
    }

    public void setTitle (String title) {
        this.title = title;
    }
    
    public String getAuthor () {
        return title;
    }
    
    public void setAuthor (String author) {
        this.author=author;
    }


}
